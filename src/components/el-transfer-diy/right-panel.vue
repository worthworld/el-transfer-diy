<template>
  <div class="el-transfer-panel">
    <p class="el-transfer-panel__header">
      <el-checkbox
        v-model="allChecked"
        :indeterminate="isIndeterminate"
        @change="handleAllCheckedChange"
      >
        {{ title }}
        <span>{{ checkedSummary }}</span>
      </el-checkbox>
    </p>

    <div
      :class="['el-transfer-panel__body', hasFooter ? 'is-with-footer' : '']"
    >
      <el-input
        v-if="filterable"
        v-model="query"
        class="el-transfer-panel__filter"
        size="small"
        :placeholder="placeholder"
        @mouseenter.native="inputHover = true"
        @mouseleave.native="inputHover = false"
      >
        <i
          slot="prefix"
          :class="['el-input__icon', 'el-icon-' + inputIcon]"
          @click="clearQuery"
        ></i>
      </el-input>

      <el-collapse v-model="currentGroup" accordion @change="handleChange">
        <el-collapse-item
          v-for="(item, index) in filteredData"
          :key="index"
          :title="item.label"
          :name="index"
        >
          <el-checkbox-group
            v-show="!hasNoMatch && data.length > 0"
            v-model="checked"
            :class="{ 'is-filterable': filterable }"
            class="el-transfer-panel__list"
          >
            <el-checkbox
              v-for="subItem in item[childrenProp]"
              :key="subItem[keyProp]"
              class="el-transfer-panel__item"
              :label="subItem[keyProp]"
              :disabled="subItem[disabledProp]"
            >
              <option-content :option="subItem"></option-content>
            </el-checkbox>
          </el-checkbox-group>
          <p v-show="hasNoMatch" class="el-transfer-panel__empty">
            {{ t("el.transfer.noMatch") }}
          </p>
          <p
            v-show="data.length === 0 && !hasNoMatch"
            class="el-transfer-panel__empty"
          >
            {{ t("el.transfer.noData") }}
          </p>
        </el-collapse-item>
      </el-collapse>

      <!-- <el-checkbox-group
